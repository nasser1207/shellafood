/**
 * Get related products (same store, different product)
 */
export function getRelatedProducts(currentProductId: string, storeId?: string, limit: number = 4): Product[] {
	const currentProduct = TEST_PRODUCTS.find(p => p.id === currentProductId);
	if (!currentProduct) return [];
	
	return TEST_PRODUCTS
		.filter(p => 
			p.id !== currentProductId && 
			(!storeId || p.storeId === storeId)
		)
		.slice(0, limit);
}

/**
 * Get recommended products for a store
 */
export function getRecommendedProducts(storeId: string, limit: number = 4): Product[] {
	return TEST_PRODUCTS
		.filter(p => p.storeId === storeId)
		.filter(p => p.badge || (p.originalPrice && p.originalPrice > (p.price || 0)))
		.slice(0, limit);
}

/**
 * Get popular products for a store
 */
export function getPopularProducts(storeId: string, limit: number = 6): Product[] {
	return TEST_PRODUCTS
		.filter(p => p.storeId === storeId)
		.sort((a, b) => (b.rating || 0) - (a.rating || 0))
		.slice(0, limit);
}

