/**
 * Get stores by category slug (English URL-friendly identifier)
 */
export function getStoresByCategorySlug(slug: string): Store[] {
	const category = TEST_CATEGORIES.find(c => c.slug === slug);
	if (!category) return [];
	
	return TEST_STORES.filter(s => s.categoryId === category.id);
}

/**
 * Get category by slug
 */
export function getCategoryBySlug(slug: string): (Category & { slug: string }) | undefined {
	return TEST_CATEGORIES.find(c => c.slug === slug);
}

/**
 * Get stores by category name (legacy support)
 */
export function getStoresByCategory(categoryName: string): Store[] {
	const category = TEST_CATEGORIES.find(c => c.name === categoryName);
	if (!category) return [];
	
	return TEST_STORES.filter(s => s.categoryId === category.id);
}

export function findStoreBySlug(slug: string): Store | null {
	return TEST_STORES.find(s => s.slug === slug) || null;
}

export function findStoreByName(name: string): Store | null {
	return TEST_STORES.find(s => s.name === name || s.nameAr === name) || null;
}

export function findStoreById(id: string): Store | null {
	return TEST_STORES.find(s => s.id === id) || null;
}

/**
 * Get products by store ID
 */
export function getProductsByStore(storeId: string): Product[] {
	return TEST_PRODUCTS.filter(p => p.storeId === storeId);
}

export function getDepartmentBySlug(slug: string): Department | null {
	return TEST_DEPARTMENTS.find(d => d.slug === slug) || null;
}

export function getProductsByStoreAndDepartmentSlug(storeId: string, departmentSlug: string): Product[] {
	const department = getDepartmentBySlug(departmentSlug);
	if (!department) return [];
	
	return TEST_PRODUCTS.filter(
		p => p.storeId === storeId && 
		(p.department === department.name || 
		 p.department?.toLowerCase() === department.name.toLowerCase())
	);
}

export function getProductsByStoreAndDepartment(storeId: string, departmentName: string): Product[] {
	const department = TEST_DEPARTMENTS.find(
		d => d.name === departmentName || d.nameAr === departmentName
	);
	
	const targetDepartmentName = department?.name || departmentName;
	
	return TEST_PRODUCTS.filter(
		p => p.storeId === storeId && 
		(p.department === targetDepartmentName || 
		 p.department?.toLowerCase() === targetDepartmentName.toLowerCase())
	);
}

export function findProductBySlug(slug: string): Product | null {
	return TEST_PRODUCTS.find(p => p.slug === slug) || null;
}

export function findProductByName(productName: string): Product | null {
	return TEST_PRODUCTS.find(p => p.name === productName || p.nameAr === productName) || null;
}

/**
 * Get related products (same store, different product)
 */
export function getRelatedProducts(currentProductId: string, storeId?: string, limit: number = 4): Product[] {
	const currentProduct = TEST_PRODUCTS.find(p => p.id === currentProductId);
	if (!currentProduct) return [];
	
	return TEST_PRODUCTS
		.filter(p => 
			p.id !== currentProductId && 
			(!storeId || p.storeId === storeId)
		)
		.slice(0, limit);
}

/**
 * Get recommended products for a store
 */
export function getRecommendedProducts(storeId: string, limit: number = 4): Product[] {
	return TEST_PRODUCTS
		.filter(p => p.storeId === storeId)
		.filter(p => p.badge || (p.originalPrice && p.originalPrice > (p.price || 0)))
		.slice(0, limit);
}

/**
 * Get popular products for a store
 */
export function getPopularProducts(storeId: string, limit: number = 6): Product[] {
	return TEST_PRODUCTS
		.filter(p => p.storeId === storeId)
		.sort((a, b) => (b.rating || 0) - (a.rating || 0))
		.slice(0, limit);
}

